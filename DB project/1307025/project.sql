CREATE TABLE INFORMATION(
	MOVIE_ID VARCHAR(50) NOT NULL,
	NAME VARCHAR(30),
	REALEASE_YEAR NUMBER(10),
	GENRE VARCHAR(30),
	INT_BOG VARCHAR(20),
	ORIGIN_COUNTRY VARCHAR(30)
);
CREATE TABLE RECOGNITOIN(
	RECMOVIE_ID VARCHAR(30),
	OSCARS VARCHAR(30),
	GOLDEN_GLOBE VARCHAR(30),
	CANNES VARCHAR(30)
);
CREATE TABLE RATINGS(
	RMOVIE_ID VARCHAR(30),
	IMDB NUMBER(4,2),
	RT_FANS NUMBER(4,2),
	RT_CRITICS NUMBER(4,2)
);
CREATE TABLE CASTCREW(
	CMOVIE_ID VARCHAR(10),
	PRODUCER VARCHAR(30),
	DIRECTOR VARCHAR(30),
	LEAD_ACTOR VARCHAR(30),
	SUP_ACTOR VARCHAR(30),
	LEAD_ACTRESS VARCHAR(30),
	SUP_ACTRESS VARCHAR(30)
);

ALTER TABLE INFORMATION add constraint pkmid primary key(MOVIE_ID);
ALTER TABLE RECOGNITOIN add constraint fkrecmid foreign key(RECMOVIE_ID) references INFORMATION(MOVIE_ID) on delete cascade;
ALTER TABLE RATINGS add constraint fkrmid foreign key(RMOVIE_ID) references INFORMATION(MOVIE_ID) on delete cascade;
ALTER TABLE CASTCREW add constraint fkcmid foreign key(CMOVIE_ID) references INFORMATION(MOVIE_ID) on delete cascade;

